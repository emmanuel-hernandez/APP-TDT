   c.latitude,
   c.longitude,
   c.acesli,
   c.effectiveDateStart,
   c.effectiveDateEnd
FROM channel c
INNER JOIN population p ON c.populationId = p.populationId
INNER JOIN state s ON s.stateId = p.stateId
INNER JOIN concessionType ct ON ct.concessionTypeId = c.concessionTypeId
